/** Зал музея (этаж, помещение). */
export interface Hall {
  id: number;
  /** Номер зала на плане экспозиции (1-11 для Музея Фаберже) */
  hallNumber: number;
  /** Название зала (напр. «Синяя гостиная») */
  name: string;
  /** Короткое описание зала, показывается в шапке экрана зала */
  shortDescription: string;
  /** Подробное описание зала (для блока "Описание зала") */
  description?: string;
  /** Заглавное фото зала */
  coverImageUrl?: string;
}
