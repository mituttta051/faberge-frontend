/** Зал музея (этаж, помещение). */
export interface Hall {
  id: number;
  /** Номер зала на плане экспозиции (1-11 для Музея Фаберже) */
  hallNumber: number;
  /** Название зала (напр. «Синяя гостиная») */
  name?: string;
  /** Описание зала (для блока «Описание зала» и шапки) */
  description?: string;
  /** Этаж/уровень в здании музея */
  level?: number;
  /** Заглавное фото зала */
  coverImageUrl?: string;
  showcaseCount?: number;
  exhibitCount?: number;
  /** Помечен ли зал как временная выставка (отдельный каталог + бейдж). */
  isTemporary?: boolean;
  /**
   * Позиция зала в каталоге (drag-n-drop в админке). Сервер уже отдаёт залы
   * отсортированными по нему, поэтому порядок массива — источник правды;
   * само поле нужно только чтобы показать позицию и отличить «не задан» (0).
   */
  sortOrder?: number;
}
